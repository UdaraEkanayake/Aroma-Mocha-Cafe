    private void btnGenerateBillPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillPrintActionPerformed
        // TODO add your handling code here:
        //generate the bill pdf
        String customerName = txtCusName.getText();
        String customerMobileNumber = txtCusMobileNo.getText();
        String customerEmail = txtCusEmail.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String todaydate = dFormat.format(date);
        String total = String.valueOf(grandTotal);
        String createdBy =userEmail;
        Bill bill = new Bill();
        bill.setId(billId);
        bill.setName(customerName);
        bill.setMobileNumber(customerMobileNumber);
        bill.setEmail(customerEmail);
        bill.setDate(todaydate);
        bill.setTotal(total);
        bill.setCreatedBy(createdBy);
        BillDao.save(bill);
        
        //creating document
        String path = "/Users/udaraekanayake96/Documents/";
        com.itextpdf.text.Document doc =new com.itextpdf.text.Document();
        try
        {
            PdfWriter.getInstance(doc, new FileOutputStream(path + "" + billId+".pdf"));
            doc.open();
            Paragraph cafeName = new Paragraph("                                                          Aroma Mocha Cafe\n");
            doc.add(cafeName);
            Paragraph starLine = new Paragraph("----------------------------------------------------------------------------------------------------------------------------------\n");
            doc.add(starLine);
            Paragraph paragraph3 = new Paragraph("\tBill ID : "+billId+"\nCustomer Name : "+customerName+"\nTotal Paid :Rs."+grandTotal);
            doc.add(paragraph3);
            doc.add(starLine);
            PdfPTable tb1 = new PdfPTable(4);
            tb1.addCell("Name");
            tb1.addCell("Price(Rs.)");
            tb1.addCell("Quantity");
            tb1.addCell("Total(Rs.)");
            for(int i=0 ; i< tblDetails.getRowCount();i++)
            {
                String n =tblDetails.getValueAt(i,0).toString();
                 String d =tblDetails.getValueAt(i,1).toString();
                  String r =tblDetails.getValueAt(i,2).toString();
                   String q =tblDetails.getValueAt(i,3).toString();
                   tb1.addCell(n);
                   tb1.addCell(d);
                   tb1.addCell(r);
                   tb1.addCell(q);
            }
            doc.add(tb1);
            doc.add(starLine);
            Paragraph thanksMsg = new Paragraph("Thank you , Please come again !!!");
            doc.add(thanksMsg);
            //open pdf
            OpenPdf.openById(String.valueOf(billId));
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        doc.close();
        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);
    }//GEN-LAST:event_btnGenerateBillPrintActionPerformed

    private void tblDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDetailsMouseClicked
        // TODO add your handling code here:
        int index = tblDetails.getSelectedRow();
        int a =JOptionPane.showConfirmDialog(null,"Do you want to remove this product ","Select",JOptionPane.YES_NO_OPTION);
        if(a==0)
        {
            TableModel model = tblDetails.getModel();
            String total = model.getValueAt(index, 3).toString();
            grandTotal = grandTotal - Integer.parseInt(total);
            lblGrandTotal.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) tblDetails.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_tblDetailsMouseClicked


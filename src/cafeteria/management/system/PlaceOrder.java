    public void productNameByCategory(String category)
    {
        //current product names load to the Name column
        DefaultTableModel dtm = (DefaultTableModel) tblName.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> list = ProductDao.getAllRecordsByCategory(category);
        Iterator<Product> itr = list.iterator ();
        while(itr.hasNext())
        {
            Product productObj = itr.next();
            dtm.addRow(new Object[] {productObj.getName()});
        }
    }
     public void filterProductByName(String name,String category)
    {
        //search the product by there name and filter them when user search for something...and only search things visible in the name column
        DefaultTableModel dtm = (DefaultTableModel) tblName.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> list = ProductDao.filterProductByName(name,category);
        Iterator<Product> itr = list.iterator ();
        while(itr.hasNext())
        {
            Product productObj = itr.next();
            dtm.addRow(new Object[] {productObj.getName()});
        }
    }
     
